    private void jButtonAddNewTopicActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddNewTopicActionPerformed
        idPerguntaActivo = -1;
        newTema = jTextFieldNewTopic.getText();
        if (!(newTema.equals(""))) {
            temaActivo = newTema;
            DadosQuestionario.gravaNovoTema(liga,newTema);
            DadosQuestionario.lerTemasToComboBox(liga,jComboBoxTopic);
            ComboBoxModel modelo = jComboBoxTopic.getModel();
            modelo.setSelectedItem(temaActivo);
        } else {
            JOptionPane.showMessageDialog(jPanelTopic, "New Topic is EMPTY!");
        }
        
    }//GEN-LAST:event_jButtonAddNewTopicActionPerformed

    private void jTableQuestionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableQuestionMouseClicked
        int linha = jTableQuestion.getSelectedRow();
        idPerguntaActivo = -1;
        if (linha >= 0) {
            String perg = jTableQuestion.getValueAt(linha, 0).toString();
            DefaultTableModel modelo = (DefaultTableModel) jTableAnswers.getModel();
            modelo.setRowCount(0);
            for (Questionario q : tQ) {
                if (q.getTema().equals(temaActivo) && (perg.equals(q.getPergunta()))) {
                    idPerguntaActivo = q.getIdPergunta();
                    ArrayList<Resposta> tr = q.getRespostas();
                    for (int i = 0; i < tr.size(); i++) {
                        Object[] dados = {(i+1),tr.get(i).getDescricao(),tr.get(i).getValor()};
                        modelo.addRow(dados);
                    }
                }
            }
        } 
        
    }//GEN-LAST:event_jTableQuestionMouseClicked


    private void jButtonAddTableAnswerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddTableAnswerActionPerformed
        jDialogAddAnswer.setVisible(false);
        // Adicionar na tabela de respostas
        String text = jTextFieldTextAddAnswer.getText();
        if (!(text.equals(""))) {
            int result = Integer.parseInt(jTextFieldResultAddAnswer.getText());
            DefaultTableModel modeloR = (DefaultTableModel) jTableAnswers.getModel();
            int nLinhas = modeloR.getRowCount();
            Object[] dados = {nLinhas, text, result};
            modeloR.addRow(dados);            
        } else {
            JOptionPane.showMessageDialog(jDialogBasicActSettings, "New Answer is EMPTY!");
        }
        jTextFieldTextAddAnswer.setText("");
    }//GEN-LAST:event_jButtonAddTableAnswerActionPerformed

    private void jButtonSaveQandAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveQandAActionPerformed
        DefaultTableModel modeloR = (DefaultTableModel) jTableAnswers.getModel();
//        System.out.println("ID Pergunta: "+idPerguntaActivo);
        if (idPerguntaActivo>0){
            DadosQuestionario.removeResposta(liga,idPerguntaActivo);
            for (int i = 0; i < modeloR.getRowCount(); i++) {
                String resposta = modeloR.getValueAt(i, 1).toString();
                int valor = Integer.parseInt(modeloR.getValueAt(i, 2).toString());
                DadosQuestionario.gravaNovaResposta(liga,idPerguntaActivo, resposta, valor);
            }
            tQ.removeAll(tQ);
            tQ = DadosQuestionario.gettQuestionario(liga);
        } else {
            JOptionPane.showMessageDialog(jDialogBasicActSettings, "ID Pergunta incorrect!");
        }
    }//GEN-LAST:event_jButtonSaveQandAActionPerformed

  
    

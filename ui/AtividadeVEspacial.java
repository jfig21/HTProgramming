    private void runClock(int time) {
        try {
            while ((runFlag) && (time >= 0)) {
                ClockThread.sleep(1000);
                int minutos = time / 60;
                int segundos = time % 60;
                if (time <= 15) {
                    jLabelTempo.setForeground(Color.red);
                }
                jLabelTempo.setText(String.format("%02d:%02d", minutos, segundos));
                time--;
                if (time < 0) {
                    runFlag = false;
                }
            }
            // Termina Questoes
            jButtonQuestoesSeguinte.setText("Terminar");
            questaoAtual = 20;
            if (time < 0) verificaRespostas();
            jButtonQuestoesSeguinte.setEnabled(false);
        } catch (InterruptedException ex) {
            Logger.getLogger(ClockThread.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    

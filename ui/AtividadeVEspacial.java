    private void jLabelMouseClicked(java.awt.event.MouseEvent evt, char op) {
        String name = "escolha";
        if (labelEscolha[op - 'A'].getName().equals(name)) {
            labelEscolha[op - 'A'].setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(255, 255, 255), 5));
            labelEscolha[op - 'A'].setName("");
            escolha = 'X';
        } else {
            for (int i = 0; i < 5; i++) {
                labelEscolha[i].setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(255, 255, 255), 5));
                labelEscolha[i].setName("");
            }
            labelEscolha[op - 'A'].setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(0, 255, 0), 5));
            labelEscolha[op - 'A'].setName("escolha");
            escolha = op;
        }
    }



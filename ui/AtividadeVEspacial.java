    private void verificaRespostas(){
       
        DefaultTableModel modelo = (DefaultTableModel)jTableResultadosVE.getModel();
        modelo.setRowCount(0);
        jTableResultadosVE.setDefaultRenderer(Object.class, new CustomRenderer());
        
        TableColumnModel columnModel = jTableResultadosVE.getColumnModel();
        columnModel.getColumn(0).setPreferredWidth(30);
        columnModel.getColumn(1).setPreferredWidth(30);
        columnModel.getColumn(2).setPreferredWidth(30);
        columnModel.getColumn(3).setPreferredWidth(120);

        String respostasfinal = "";
        int nacertos = 0;
        
        for (int i = 0; i < 20; i++) {
            if (respostas[i]==solucao[i]){
                respostasfinal += "1";
                nacertos++;
            } else {
                respostasfinal += "0";
            }
            Object []dados = {(i+1), // Número Questão
                            solucao[i],
                            respostas[i],
                            ((respostasfinal.charAt(i)=='1')?"Resposta Correta":"Resposta Incorreta")
            };
            
            modelo.addRow(dados);
            
                       
        }
        
        jLabelOutResultadosVE.setText("Resultado: "+nacertos+"/20");
        jTabbedPaneVEspacial.add(jPanelResultados);
        jTabbedPaneVEspacial.setTitleAt(3, "Resultados");
        jTabbedPaneVEspacial.setSelectedIndex(3);
        jTabbedPaneVEspacial.repaint();
        
        DadosVEspacial dados = new DadosVEspacial(nacertos, respostasfinal);

    }
    
    
    
